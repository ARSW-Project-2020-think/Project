from requests import get
print("Localhost")
z = get("http://localhost:8080/user/login",data='{"username":"jay@mail.com"}',headers={"Content-Type":"application/json"})
print(z)
w = z.json()
print(w)
print(get("http://localhost:8080/api",headers={"Authorization":w["token"]}).text)
